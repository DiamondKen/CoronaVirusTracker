function fetchData() {
  const response = fetch(
    "https://api.covid19api.com/total/country/united-states/status/confirmed"
  )
    .then((res) => res.json())
    .then((result) => result.map(returnData));
  return response;
}

function finalizeData() {
  const res = fetchData();
  let data = [{ id: "United States", color: "hsl(40, 70%, 50%)", data: [res] }];
  return data;
}

function returnData(item) {
  return { x: item.Date, y: item.Cases };
}

